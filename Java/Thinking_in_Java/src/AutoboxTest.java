public class AutoboxTest {
	public static void main(String[] args) {
		boolean a = true;
		char b = 'b';
		int c = 9;
		float d = 9.9f;
		Boolean A = a;
		Character B = b;
		Integer C = c;
		Float D = d;
		System.out.println("a = " + a);
		System.out.println("A = " + A);
		System.out.println("b = " + b);
		System.out.println("B = " + B);
		System.out.println("c = " + c);
		System.out.println("C = " + c);
		System.out.println("d = " + d);
		System.out.println("D = " + d);
	}
}
